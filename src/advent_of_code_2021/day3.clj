(defn b-to-int [b]
  (reduce +
          (map * (reverse b) (iterate (partial * 2) 1))))

(defn strip-collection [input strip]
  (map #(nth %1 strip) input))

(defn highest-freq? [m] (reduce (fn [[rk rv :as r] [vk vv :as v]] (if (> vv rv) v (if (= vv rv) (if (= vk 1) v r) r))) m))
(defn lowest-freq?  [m] (reduce (fn [[rk rv :as r] [vk vv :as v]] (if (< vv rv) v (if (= vv rv) (if (= vk 0) v r) r))) m))

(defn gamma_epsilon [input]
  (let [strips (map (partial strip-collection input) (take (count (first input)) (iterate inc 0)))
        strips-freq (map frequencies strips)]
    [(b-to-int (map first (map highest-freq? strips-freq)))
     (b-to-int (map first (map lowest-freq? strips-freq)))]))

(defn life_support_rating [input pred]
  (loop [input input
         i 0]
    (let [strip (strip-collection input i)
          strip-freq (frequencies strip)
          matcher (pred strip-freq)
          filtered_input (filter
                          (fn [input] (if (= (nth input i) (first matcher)) true false))
                          input)]
      (if (= 1 (count filtered_input))
        (b-to-int (first filtered_input))
        (recur filtered_input (inc i))))))


